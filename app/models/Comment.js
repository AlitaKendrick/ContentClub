// *** Dependencies
// =============================================================
var mongoose = require('mongoose');

// =============================================================
// *** Create Schema
// =============================================================
var Schema = mongoose.Schema;

var Comment = new Schema ({
	/*username: {
		type: String,
		required: true
	},*/
	text: {
		type: String,
		minlength: 1
	},
	createdAt: {
    type: Date,
    default: Date.now
  	}
});


// =============================================================
// *** Create Comment Model
// =============================================================
var Comment = mongoose.model('Comment', Comment);

// =============================================================
// *** Export the Comment Model
// =============================================================
module.exports = Comment;